#pragma once

#define RAM_BASE_PHY          0x00000000
#define RAM_BASE_BUS_UNCACHED 0xc0000000
#define RAM_BASE_BUS_CACHED   0x40000000

#define RAM_PHY_TO_BUS_UNCACHED(x) ((NARROW_PTR(x) & 0x00ffffff) | RAM_BASE_BUS_UNCACHED) 

#define RAM_PHY_TO_BUS_CACHED(x) ((NARROW_PTR(x) & 0x00ffffff) | RAM_BASE_BUS_CACHED) 

#define PERIPHERAL_ADDR_RANGE_START 0x3f000000 
#define PERIPHERAL_ADDR_RANGE_END   0x40000000
#define PERIPHERAL_BASE_PHY         PERIPHERAL_ADDR_RANGE_START
#define PERIPHERAL_BASE_BUS   0x7e000000

#define LOCAL_PERIPH_ADDR_START 0x40000000
#define LOCAL_PERIPH_ADDR_END   0x40002000

#define NARROW_PTR(x) ((uint32_t)((uint64_t)(x)))

#define PERIPHERAL_PHY_TO_BUS(x) ((NARROW_PTR(x) & 0x00ffffff) + PERIPHERAL_BASE_BUS) 


